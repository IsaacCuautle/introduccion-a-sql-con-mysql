-- FULL JOIN | CONSULTA LOS CLIENTES QUE NO TIENEN VENDEDOR
SELECT
	C.NOMBRE AS CLIENTE,
    C.BARRIO,
    V.NOMBRE AS VENDEDOR
FROM
	tabla_de_clientes AS C
    LEFT JOIN
    tabla_de_vendedores AS V
    ON C.BARRIO = V.BARRIO
UNION
SELECT
	C.NOMBRE AS CLIENTE,
    C.BARRIO,
    V.NOMBRE AS VENDEDOR
FROM
	tabla_de_clientes AS C
    RIGHT JOIN
    tabla_de_vendedores AS V
    ON C.BARRIO = V.BARRIO;
