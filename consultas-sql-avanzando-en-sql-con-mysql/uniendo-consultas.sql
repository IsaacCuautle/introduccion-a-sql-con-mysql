-- CONSULTA LOS BARRIOS EN COMUN SIN REPETIR
SELECT 
	BARRIO
FROM
	tabla_de_clientes
UNION    
SELECT
	BARRIO
FROM
	tabla_de_vendedores;
    
-- CONSULTA LOS BARRIOS EN COMUN REPETIDOS
SELECT 
	BARRIO
FROM
	tabla_de_clientes
UNION ALL    
SELECT
	BARRIO
FROM
	tabla_de_vendedores;

-- CONSULTA LOS BARRIOS Y NOMBRES EN COMUN SIN REPETIR
